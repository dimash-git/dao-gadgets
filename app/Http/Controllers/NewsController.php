     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required|string|max:255',
            'description' => 'required|string',
            'date' => 'required|date',
            'img' => 'string|nullable'
        ]);

        $news = News::create([
            'user_id' => auth()->id(),
            'title' => $request->title,
            'description' => $request->description,
            'date' => $request->date,
            'img' => $request->img
        ]);

        return response()->json($news, 201);
    }

